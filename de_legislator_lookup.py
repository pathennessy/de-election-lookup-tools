def geocode_address(session: requests.Session, address: str) -> Optional[dict]:
    """
    Geocode a single-line address using the Delaware FirstMap locator.
    Returns the top candidate dict or None if no match found.
    """
    params = {
        "SingleLine": address,
        "maxLocations": 1,
        "outSR": json.dumps({"wkid": 102100}),  # Web Mercator, matches FeatureServer
        "f": "json",
    }
    try:
        resp = session.get(GEOCODE_URL, params=params, timeout=15)
        resp.raise_for_status()
        data = resp.json()
    except Exception as exc:
        log.warning("Geocode request failed for '%s': %s", address, exc)
        return None

    candidates = data.get("candidates", [])
    if not candidates:
        log.warning("No geocode match for: %s", address)
        return None

    return candidates[0]  # keys: address, location {x, y}, score


def query_layer(
    session: requests.Session,
    layer_id: int,
    x: float,
    y: float,
    fields: list,
) -> Optional[dict]:
    """
    Point-in-polygon query against a FeatureServer layer.
    Returns the attributes dict of the first matching feature, or None.
    """
    params = {
        "f": "json",
        "geometry": json.dumps({"x": x, "y": y}),
        "geometryType": "esriGeometryPoint",
        "inSR": "102100",
        "spatialRel": "esriSpatialRelIntersects",
        "outFields": ",".join(fields),
        "returnGeometry": "false",
        "resultRecordCount": 500,
    }
    url = f"{FEATURE_SERVER_BASE}/{layer_id}/query"
    try:
        resp = session.get(url, params=params, timeout=15)
        resp.raise_for_status()
        data = resp.json()
    except Exception as exc:
        log.warning("Layer %d query failed: %s", layer_id, exc)
        return None

    features = data.get("features", [])
    if not features:
        return None
    return features[0].get("attributes", {})


def lookup_address(session: requests.Session, address: str) -> dict:
    """
    Full lookup: geocode + senate and house layer queries.
    Returns a flat dict of all output fields.
    """
    # Build result with empty defaults for every output column
    result = {}
    for entry in OUTPUT_COLUMNS:
        col_name = entry[2] if len(entry) == 3 else entry[1]
        result[col_name] = ""

    # Geocode
    candidate = geocode_address(session, address)
    if not candidate:
        result["geocoded_address"] = "NOT FOUND"
        return result

    loc = candidate["location"]
    x, y = loc["x"], loc["y"]
    result["geocoded_address"] = candidate.get("address", address)
    result["geocode_score"] = str(candidate.get("score", ""))

    # Query each FeatureServer layer
    for layer_key, layer_cfg in LAYERS.items():
        attrs = query_layer(session, layer_cfg["id"], x, y, layer_cfg["fields"])
        if not attrs:
            continue
        for entry in OUTPUT_COLUMNS:
            if entry[0] != layer_key:
                continue
            field = entry[1]
            col_name = entry[2] if len(entry) == 3 else entry[1]
            val = attrs.get(field)
            result[col_name] = "" if val is None else str(val)

    return result


