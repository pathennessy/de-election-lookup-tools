def main() -> None:
    parser = argparse.ArgumentParser(
        description="Look up Delaware legislative district info for addresses in a CSV."
    )
    parser.add_argument("input", help="Path to input CSV file")
    parser.add_argument(
        "--address-col", "-a",
        metavar="COLUMN",
        help="Name of the column containing addresses (auto-detected if omitted)",
    )
    parser.add_argument(
        "--output", "-o",
        metavar="FILE",
        default="output.csv",
        help="Path for the output CSV (default: output.csv)",
    )
    parser.add_argument(
        "--delay", "-d",
        type=float,
        default=0.25,
        metavar="SECONDS",
        help="Pause between address lookups to be polite to the API (default: 0.25s)",
    )
    args = parser.parse_args()

    process_csv(
        input_path=args.input,
        output_path=args.output,
        address_col=args.address_col,
        delay=args.delay,
    )


if __name__ == "__main__":
    main()
