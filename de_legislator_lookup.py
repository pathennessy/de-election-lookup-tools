def detect_address_column(fieldnames: list) -> Optional[str]:
    """Heuristic: find a column whose name suggests it holds addresses."""
    for name in fieldnames:
        low = name.lower().strip()
        if low in ("address", "addr", "street", "street_address",
                   "full_address", "mailing_address", "property_address"):
            return name
    return None


def build_output_fieldnames(input_fields: list) -> list:
    output_cols = [
        entry[2] if len(entry) == 3 else entry[1]
        for entry in OUTPUT_COLUMNS
    ]
    return input_fields + output_cols


def process_csv(
    input_path: str,
    output_path: str,
    address_col: Optional[str],
    delay: float,
) -> None:
    with open(input_path, newline="", encoding="utf-8-sig") as f_in:
        reader = csv.DictReader(f_in)
        if not reader.fieldnames:
            log.error("Input CSV has no header row.")
            sys.exit(1)

        # Resolve address column
        col = address_col or detect_address_column(reader.fieldnames)
        if not col:
            log.error(
                "Could not auto-detect address column. "
                "Use --address-col to specify. Available columns: %s",
                ", ".join(reader.fieldnames),
            )
            sys.exit(1)
        if col not in reader.fieldnames:
            log.error(
                "Column '%s' not found. Available: %s",
                col,
                ", ".join(reader.fieldnames),
            )
            sys.exit(1)

        log.info("Using address column: '%s'", col)
        out_fields = build_output_fieldnames(list(reader.fieldnames))

        with open(output_path, "w", newline="", encoding="utf-8") as f_out:
            writer = csv.DictWriter(f_out, fieldnames=out_fields, extrasaction="ignore")
            writer.writeheader()

            rows = list(reader)
            total = len(rows)
            for i, row in enumerate(rows, 1):
                address = row.get(col, "").strip()
                log.info("[%d/%d] Looking up: %s", i, total, address)

                if not address:
                    lookup = {
                        entry[2] if len(entry) == 3 else entry[1]: ""
                        for entry in OUTPUT_COLUMNS
                    }
                    lookup["geocoded_address"] = "SKIPPED (empty)"
                else:
                    session = requests.Session()
                    session.headers["User-Agent"] = "de-legislator-lookup/1.0"
                    lookup = lookup_address(session, address)

                writer.writerow({**row, **lookup})

                if delay > 0 and i < total:
                    time.sleep(delay)

    log.info("Done. Output written to: %s", output_path)


