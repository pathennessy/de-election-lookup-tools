def geocode_address(session: requests.Session, address: str) -> Optional[dict]:
    """Return the top geocode candidate dict, or None."""
    params = {
        "SingleLine": address,
        "maxLocations": 1,
        "outSR": json.dumps({"wkid": 102100}),
        "f": "json",
    }
    try:
        resp = session.get(GEOCODE_URL, params=params, timeout=15)
        resp.raise_for_status()
        data = resp.json()
    except Exception as exc:
        log.warning("Geocode failed for '%s': %s", address, exc)
        return None

    candidates = data.get("candidates", [])
    if not candidates:
        log.warning("No geocode match for: %s", address)
        return None
    return candidates[0]


def query_political_layer(
    session: requests.Session,
    layer_id: int,
    x: float,
    y: float,
    fields: list,
) -> Optional[dict]:
    """Point-in-polygon query against DE_Political_Boundaries FeatureServer."""
    params = {
        "f": "json",
        "geometry": json.dumps({"x": x, "y": y}),
        "geometryType": "esriGeometryPoint",
        "inSR": "102100",
        "spatialRel": "esriSpatialRelIntersects",
        "outFields": ",".join(fields),
        "returnGeometry": "false",
        "resultRecordCount": 500,
        "where": "1=1",
    }
    url = f"{FEATURE_SERVER_BASE}/{layer_id}/query"
    try:
        resp = session.get(url, params=params, timeout=15)
        resp.raise_for_status()
        data = resp.json()
    except Exception as exc:
        log.warning("Layer %d query failed: %s", layer_id, exc)
        return None

    if data.get("exceededTransferLimit") and not data.get("features"):
        log.warning("Layer %d returned exceededTransferLimit with 0 features", layer_id)
        return None

    features = data.get("features", [])
    return features[0].get("attributes", {}) if features else None


def query_county_officials(
    session: requests.Session,
    x: float,
    y: float,
) -> tuple[Optional[str], Optional[str], list]:
    """
    Two-step county officials lookup.
    Returns (county_name, district_label, officials_list).
    officials_list is [{office, name, title}, ...].
    """
    # Step 1 — spatial query → OBJECTID + labels
    params1 = {
        "f": "json",
        "geometry": json.dumps({"x": x, "y": y}),
        "geometryType": "esriGeometryPoint",
        "inSR": "102100",
        "spatialRel": "esriSpatialRelIntersects",
        "outFields": "OBJECTID,COUNTY,DISTRICT",
        "returnGeometry": "false",
        "resultRecordCount": 500,
    }
    try:
        resp = session.get(f"{COUNTY_OFFICIALS_URL}/query", params=params1, timeout=15)
        resp.raise_for_status()
        data1 = resp.json()
    except Exception as exc:
        log.warning("County officials spatial query failed: %s", exc)
        return None, None, []

    features = data1.get("features", [])
    if not features:
        log.warning("No county officials district found for (%.0f, %.0f)", x, y)
        return None, None, []

    attrs1 = features[0]["attributes"]
    oid = attrs1.get("OBJECTID")
    county = str(attrs1.get("COUNTY") or "")
    district = str(attrs1.get("DISTRICT") or "")

    if oid is None:
        return county, district, []

    # Step 2 — related records → official roster
    params2 = {
        "f": "json",
        "objectIds": str(oid),
        "outFields": "County,Office,Name,Title",
        "relationshipId": 0,
        "returnGeometry": "false",
    }
    try:
        resp = session.get(
            f"{COUNTY_OFFICIALS_URL}/queryRelatedRecords", params=params2, timeout=15
        )
        resp.raise_for_status()
        data2 = resp.json()
    except Exception as exc:
        log.warning("County officials related records query failed: %s", exc)
        return county, district, []

    officials = []
    for group in data2.get("relatedRecordGroups", []):
        for rec in group.get("relatedRecords", []):
            a = rec.get("attributes", {})
            officials.append({
                "office": (a.get("Office") or "").strip(),
                "name":   (a.get("Name")   or "").strip(),
                "title":  (a.get("Title")  or "").strip(),
            })

    return county, district, officials


def parse_county_officials(officials: list) -> dict:
    """
    Map raw official records to fixed output columns.

    Office matching order matters — check specific strings before broad ones:
      "county executive"            → county_executive        (New Castle only)
      "council president" / title   → county_council_president (NCC & Kent president)
      "clerk of the peace"          → clerk_of_peace
      "register of wills"           → register_of_wills
      "recorder of deeds"           → recorder_of_deeds
      "sheriff"                     → county_sheriff
      "council" / "levy court"      → county_council_member   (district rep)

    Kent's Levy Court president is identified by Title == "President", not by
    the Office string, so we check Title after the broad council/levy court match.
    """
    result = {
        "county_executive": "",
        "county_council_president": "",
        "county_council_member": "",
        "county_sheriff": "",
        "clerk_of_peace": "",
        "register_of_wills": "",
        "recorder_of_deeds": "",
    }
    council_members = []

    for official in officials:
        office = official["office"].lower()
        name   = official["name"]
        title  = official["title"].lower()
        if not name:
            continue

        if "county executive" in office:
            result["county_executive"] = name
        elif "council president" in office or "president of county council" in office:
            result["county_council_president"] = name
        elif "clerk of the peace" in office or "clerk of peace" in office:
            result["clerk_of_peace"] = name
        elif "register of wills" in office:
            result["register_of_wills"] = name
        elif "recorder of deeds" in office:
            result["recorder_of_deeds"] = name
        elif "sheriff" in office:
            result["county_sheriff"] = name
        elif "council" in office or "levy court" in office:
            # Levy Court / Council president is identified by the Title field
            if title == "president":
                result["county_council_president"] = name
            else:
                council_members.append(name)

    result["county_council_member"] = "; ".join(council_members)
    return result


def query_wilmington_city_council(session: requests.Session, x: float, y: float) -> dict:
    """
    Query Layer 5 (Wilmington City Council Districts).
    Returns empty strings for addresses outside Wilmington.
    """
    fields = ["NAME", "PRESIDENT", "COUNCIL_ME", "ATLARGE", "ATLARGE1", "ATLARGE2", "ATLARGE3"]
    attrs = query_political_layer(session, 5, x, y, fields)

    empty = {
        "wilm_city_council_district": "",
        "wilm_council_president": "",
        "wilm_council_member": "",
        "wilm_council_at_large": "",
    }
    if not attrs:
        return empty

    at_large = [
        attrs.get("ATLARGE")  or "",
        attrs.get("ATLARGE1") or "",
        attrs.get("ATLARGE2") or "",
        attrs.get("ATLARGE3") or "",
    ]

    return {
        "wilm_city_council_district": str(attrs.get("NAME")      or ""),
        "wilm_council_president":     str(attrs.get("PRESIDENT") or ""),
        "wilm_council_member":        str(attrs.get("COUNCIL_ME") or ""),
        "wilm_council_at_large":      "; ".join(m for m in at_large if m),
    }


