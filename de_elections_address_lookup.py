def lookup_address(session: requests.Session, address: str) -> dict:
    result = {col: "" for col in OUTPUT_COLS}

    candidate = geocode_address(session, address)
    if not candidate:
        result["geocoded_address"] = "NOT FOUND"
        return result

    loc = candidate["location"]
    x, y = loc["x"], loc["y"]
    result["geocoded_address"] = candidate.get("address", address)
    result["geocode_score"]    = str(candidate.get("score", ""))

    # State Senate (Layer 1)
    attrs = query_political_layer(session, 1, x, y, ["DISTRICT", "NAME", "URL"])
    if attrs:
        result["senate_district"] = str(attrs.get("DISTRICT") or "")
        result["senator"]         = str(attrs.get("NAME")     or "")
        result["senator_url"]     = str(attrs.get("URL")      or "")

    # State House (Layer 2)
    attrs = query_political_layer(session, 2, x, y, ["DISTRICT", "NAME", "URL"])
    if attrs:
        result["house_district"]      = str(attrs.get("DISTRICT") or "")
        result["representative"]      = str(attrs.get("NAME")     or "")
        result["representative_url"]  = str(attrs.get("URL")      or "")

    # County officials (two-step)
    county, county_district, officials = query_county_officials(session, x, y)
    result["county"]          = county or ""
    result["county_district"] = county_district or ""
    if officials:
        result.update(parse_county_officials(officials))

    # Wilmington City Council (Layer 5 — empty outside Wilmington)
    result.update(query_wilmington_city_council(session, x, y))

    # School board (Layer 4)
    result.update(query_school_board(session, x, y))

    return result


